/* operations.c */

/**
 * add - Performs addition of two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Return: The result of the addition.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Performs subtraction of two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Return: The result of the subtraction.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Performs multiplication of two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Return: The result of the multiplication.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Performs division of two integers.
 * @a: The dividend.
 * @b: The divisor.
 *
 * Return: The quotient of the division.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Performs modulo operation of two integers.
 * @a: The dividend.
 * @b: The divisor.
 *
 * Return: The remainder of the division.
 */
int mod(int a, int b)
{
	return (a % b);
}

